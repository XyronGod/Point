using System;
using System.Collections.Generic;
using System.Text;

namespace ConsoleApp12
{
	class Точка
	{
		public float x;
		public float y;
		public Точка(float x, float y)
		{
			this.x = x;
			this.y = y;
		}
		public float GetX()
		{
			return x;
		}
		public float GetY()
		{
			return y;
		}

	}
}
